package net.bobacus.util;

public class CachingFactoryException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public CachingFactoryException() {
		super();
	}

	public CachingFactoryException(String arg0) {
		super(arg0);
	}

	public CachingFactoryException(Throwable arg0) {
		super(arg0);
	}

	public CachingFactoryException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
