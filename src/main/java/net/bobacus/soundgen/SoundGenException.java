package net.bobacus.soundgen;

public class SoundGenException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3764031700929778264L;

	public SoundGenException() {
		super();
	}

	public SoundGenException(String arg0) {
		super(arg0);
	}

	public SoundGenException(Throwable arg0) {
		super(arg0);
	}

	public SoundGenException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
